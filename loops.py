# The while loop
while True:
    print('Hello world!')

# When you want to repeat the iteration 3times
a = 0
while a < 3:
    print('Hey letter a')
    a += 1

#Using the for loop to do the same
for a in [0, 1, 2]:
    print('Hello letter a') 

# Alternatively you can use a range if you are not sure how big your list is
for a in range(3):
    print('Hello letter a')

